#include<stdio.h>

int main(void)
{
    // 문자상수를 ASCII 코드 문자형식으로 출력한다.
    printf("%c\n",'A');
    // 문자 'A'의 ASCII 코드값(65)에 1을 더한 값을 문자로 출력한다.
    printf("%c\n",'A'+1);
    // 문자상수를 ASCII코드 문자형식으로 출력한다.
    printf("%c\n",'C');

    // 문자 'A'의 ASCII 코드값을 10진수(%d)로 출력한다.
    printf("%d\n",'A');
    

}